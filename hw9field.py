class Field: #Базовий клас для полів запису.
    """Представлення загальне поле зі значенням."""

    def __init__(self, value):
        """Ініціалізування поле значенням."""
        self.value = value

    def __str__(self):
        """Повернення рядкового представлення значення."""
        return str(self.value)